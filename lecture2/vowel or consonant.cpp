// vowel or consonant---
#include <iostream>
using namespace std;
int main()
{
    char c;
    {
        cout << "Enter a character: ";
        cin >> c;
    }
    if (c == 'a')
    {
        cout << "vowel" << endl;
    }
    else if (c == 'e')
    {
        cout << "vowel" << endl;
    }
    else if (c == 'i')
    {
        cout << "vowel" << endl;
    }
    else if (c == 'o')
    {
        cout << "vowel" << endl;
    }
    else if (c == 'u')
    {
        cout << "vowel" << endl;
    }
    else
    {
        cout << "consonant" << endl;
    }
}